train_data = torchvision.datasets.MNIST(root='./',
                                        train=True,
                                        transform=transforms.ToTensor())
test_data = torchvision.datasets.MNIST(root='./',
                                        train=False,
                                        transform=transforms.ToTensor())

train_loader = torch.utils.data.DataLoader(dataset=train_data,
                                           batch_size=batch_size,
                                           shuffle=True)

test_loader = torch.utils.data.DataLoader(dataset=test_data,
                                          batch_size=batch_size,
                                          shuffle=False)

image, label = next(iter(test_loader))

class Classifier(nn.Module):
  def __init__(self, intput_size, hidden_size, num_layers, num_classes,drop_percent=0.2, model="RNN"):
    super(Classifier, self).__init__()
    self.model = model

    self.hidden_size = hidden_size
    self.num_layers = num_layers
    self.rnn = nn.RNN(input_size, hidden_size, num_layers, batch_first=True)
    self.lstm = nn.LSTM(input_size, hidden_size, num_layers, batch_first=True)
    self.gru = nn.GRU(input_size, hidden_size, num_layers, batch_first=True)
    self.dropout = nn.Dropout(drop_percent)

    self.fc = nn.Linear(hidden_size, num_classes)

  def forward(self, x):
    # set initial hidden states and cell states
    h0 = torch.zeros(self.num_layers, x.size(0), self.hidden_size).to(device) # torch.size([2, 50, 128])
    c0 = torch.zeros(self.num_layers, x.size(0), self.hidden_size).to(device) # torch.size([2, 50, 128])


    if self.model == "RNN":
      out, _  = self.rnn(x,h0)
    elif self.model == "LSTM":
      out, _  = self.lstm(x, (h0, c0)) # output: tensor [batch_size, seq_length, hidden_size]
      out = self.dropout(out)
    elif self.model == "GRU":
      out, _  = self.gru(x, h0)
    else:
      print("choose a model in ['RNN', 'LSTM', 'GRU']")
      raise


    #Decode the hidden state of the last time step
    out = self.dropout(out)
    out = self.fc(out[:,-1,:])

    return out

model = Classifier(input_size, hidden_size, num_layers, num_classes, model="LSTM").to(device)

criterion = nn.CrossEntropyLoss()
optimizer = torch.optim.Adam(model.parameters(), lr=learning_rate)

####### Train #######
total_step = len(train_loader)
for epoch in range(num_epochs):
  for i, (image, label) in enumerate(train_loader):
    image = image.reshape(-1, sequence_length, input_size).to(device)
    label = label.to(device)

    # Forward
    output = model(image)
    loss = criterion(output, label)

    # Backward and optimize
    optimizer.zero_grad()
    loss.backward()
    optimizer.step()

    if (i+1) % 400 == 0:
      print("Epoch [{}/{}], Step[{}/{}], Loss:{:.4f}".format(epoch+1, num_epochs, i+1, total_step, loss.item()))

######## TEST ########
with torch.no_grad():
  correct = 0

  for image, label in test_loader:
    image = image.reshape(-1, sequence_length, input_size).to(device)
    label = label.to(device)
    output = model(image)
    _, pred = torch.max(output.data, 1)
    correct += (pred == label).sum().item()

  print('Test Accuracy of LSTM model on the {} test images: {}%'.format(len(test_data), 100 * correct / len(test_data)))



